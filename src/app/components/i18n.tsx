// ── Hooks & helpers ────────────────────────────────────────────────────────────

export function useT() {
  const { language } = useApp();
  const lang: Lang = (language in T ? language : 'en') as Lang;
  return (key: string): string => T[lang][key] ?? T.en[key] ?? key;
}

export function getQuestions(language: Lang): string[] {
  const lang: Lang = (language in T ? language : 'en') as Lang;
  return (T[lang].questions ?? T.en.questions).split('|');
}

export function getNudgePrompts(language: Lang): string[] {
  const lang: Lang = (language in T ? language : 'en') as Lang;
  return (T[lang].nudge_prompts ?? T.en.nudge_prompts).split('|');
}

export function getSlideContent(language: Lang, country: string, idx: number): { title: string; body: string } {
  const lang: Lang = (language in T ? language : 'en') as Lang;
  const base = country === 'namibia' ? 'slide_namibia' : country === 'guatemala' ? 'slide_guatemala' : 'slide_usa';
  return {
    title: T[lang][`${base}_${idx}_title`] ?? T.en[`${base}_${idx}_title`] ?? '',
    body:  T[lang][`${base}_${idx}_body`]  ?? T.en[`${base}_${idx}_body`]  ?? '',
  };
}